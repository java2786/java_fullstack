public class P1_square {
    public static void main(String[] args) {
        int rows = 3;
        int cols = rows;

        for(int i=1;i<=rows;i++){
            for(int j=1;j<=cols;j++){
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}


